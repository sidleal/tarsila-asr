# Function to compute cosine similarity
def cos_sim(a,b):
  a=a.unsqueeze(0)
  b=b.unsqueeze(0)
  a_norm = torch.nn.functional.normalize(a, p=2, dim=1)
  b_norm = torch.nn.functional.normalize(b, p=2, dim=1)
  # print(a_norm.shape)
  # print(b_norm.shape)
  ss  =  torch.mm(a_norm, b_norm.transpose(0, 1)).item()
  #print(f'SS:{ss}')
  return ss

