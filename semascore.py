# Funtion to compute (1 - match error rate)
def get_mer(ground_truth,inference):
    aligned=hit_values(ground_truth,inference)
    mismatches=0
    for i in aligned:
        if i in ('+','-','$'):
            mismatches+=1
    mer=mismatches/max(len(ground_truth),len(inference))
    return 1-mer

